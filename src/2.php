<?php
// Function to calculate the area of a rectangle
function area($length, $width) {
    return $length * $width;
}

// Test the function with different values
echo "Area of a rectangle with length 5 and width 3 is " . area(5, 3) . "\n";
echo "Area of a rectangle with length 10 and width 5 is " . area(10, 5) . "\n";
?>